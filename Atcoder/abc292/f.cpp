void solve() {
    cin>>a>>b;
    if(a>b) swap(a,b);
    double ans=a;
    if(cmp(sqrt(b*b-(b/2)*(b/2)),a)<=0) ans=b;
    double l=0,r=5000;
    while(r-l>eps) {
        double mid=(l+r)/2;
        double theta;
        if(cmp(mid,b)<=0) theta=0;
        else theta=acos(b/mid);
        if(theta<=PI/6&&mid*cos(PI/6-theta)<=a) l=mid;
        else r=mid;
    }
    ans=max(ans,l);
    cout<<ans<<"\n";
}
