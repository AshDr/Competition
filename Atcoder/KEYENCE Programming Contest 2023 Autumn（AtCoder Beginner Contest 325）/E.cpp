void solve() {
    cin >> n >> a >> b >> c;
    vector<vector<pair<int,ll>>> G(2 * n + 1);
    vector<ll> dis(2 * n + 1,1e18);
    vector<int> vis(2 * n + 1);
    for(int i = 1; i <= n; i++) {
    	for(int j = 1; j <= n; j++) {
    		cin >> d[i][j];
    		if(i != j) {
    			G[i].push_back({j, d[i][j] * a});
    			G[i].push_back({i + n, 0});
    			G[i + n].push_back({j + n, d[i][j] * b + c});
    		}
    	}
    }
    priority_queue<pair<ll,int>,vector<pair<ll,int>>,greater<pair<ll,int> >> heap;
    heap.push({0, 1});
    dis[1] = 0;
    while(!heap.empty()) {
    	auto [dd, u] = heap.top();heap.pop();
    	if(vis[u]) continue;
    	vis[u] = 1;
    	for(auto [v, w]: G[u]) {
    		if(dis[v] > dis[u] + w) {
    			dis[v] = dis[u] + w;
    			heap.push({dis[v], v});
    		}
    	}
    }
    cout << dis[2 * n] << "\n";
    
}
