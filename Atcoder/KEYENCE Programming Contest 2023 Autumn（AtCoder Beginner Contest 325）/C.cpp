struct DSU {
    std::vector<int> f, siz;
    DSU(int n) : f(n), siz(n, 1) { std::iota(f.begin(), f.end(), 0); }
    int leader(int x) {
        while (x != f[x]) x = f[x] = f[f[x]];
        return x;
    }
    bool same(int x, int y) { return leader(x) == leader(y); }
    bool merge(int x, int y) {
        x = leader(x);
        y = leader(y);
        if (x == y) return false;
        siz[x] += siz[y];
        f[y] = x;
        return true;
    }
    int size(int x) { return siz[leader(x)]; }
};
char s[N][N];
const int dx[] = {-1,1,0,0,1,1,-1,-1};
const int dy[] = {0,0,1,-1,1,-1,1,-1};
void solve() {
  	int n, m;
  	cin >> n >> m;
  	for(int i = 1; i <= n; i++) {
  		cin >> (s[i] + 1);
  	}
  	DSU dsu(n * m + 1);
  	auto check = [&](int x, int y) {
  		if(x < 1 || x > n || y < 1 || y > m) return false;
  		return true;
  	};
  	auto getid = [&](int x, int y) {
  		return (x - 1) * m + y;
  	};
  	for(int i = 1; i <= n; i++) {
  		for(int j = 1; j <= m; j++) {
			if(s[i][j] != '#') continue;
  			for(int k = 0; k < 8; k++) {
  				int x = i + dx[k],y = j + dy[k];
  				if(check(x, y) && s[x][y] == '#') {
  					dsu.merge(getid(i, j), getid(x, y));
  				}
  			}
  		}
  	}
  	int ans = 0;
  	for(int i = 1; i <= n; i++) {
  		for(int j = 1; j <= m; j++) {
  			if(getid(i, j) == dsu.leader(getid(i, j)) && s[i][j] == '#') {
  				++ans;
  			}
  		}
  	}
  	cout << ans << "\n";
}
