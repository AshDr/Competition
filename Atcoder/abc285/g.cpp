template<class T>
struct Flow {
    const int n;
    struct Edge {
        int to;
        T cap;
        Edge(int to, T cap) : to(to), cap(cap) {}
    };
    std::vector<Edge> e;
    std::vector<std::vector<int>> g;
    std::vector<int> cur, h;
    Flow(int n) : n(n), g(n) {}
    
    bool bfs(int s, int t) {
        h.assign(n, -1);
        std::queue<int> que;
        h[s] = 0;
        que.push(s);
        while (!que.empty()) {
            const int u = que.front();
            que.pop();
            for (int i : g[u]) {
                auto [v, c] = e[i];
                if (c > 0 && h[v] == -1) {
                    h[v] = h[u] + 1;
                    if (v == t) {
                        return true;
                    }
                    que.push(v);
                }
            }
        }
        return false;
    }
    
    T dfs(int u, int t, T f) {
        if (u == t) {
            return f;
        }
        auto r = f;
        for (int &i = cur[u]; i < int(g[u].size()); ++i) {
            const int j = g[u][i];
            auto [v, c] = e[j];
            if (c > 0 && h[v] == h[u] + 1) {
                auto a = dfs(v, t, std::min(r, c));
                e[j].cap -= a;
                e[j ^ 1].cap += a;
                r -= a;
                if (r == 0) {
                    return f;
                }
            }
        }
        return f - r;
    }
    void addEdge(int u, int v, T c) {
        g[u].push_back(e.size());
        e.emplace_back(v, c);
        g[v].push_back(e.size());
        e.emplace_back(u, 0);
    }
    T maxFlow(int s, int t) {
        T ans = 0;
        while (bfs(s, t)) {
            cur.assign(n, 0);
            ans += dfs(s, t, std::numeric_limits<T>::max());
        }
        return ans;
    }
};
int n,m;
void solve() {
    cin >> n >> m;
    Flow<int> flow(2*m*n+2);
    vector<string> s(n);
    for(int i = 0; i < n; i++) {
        cin >> s[i];
    }
    vector<array<int, 2>> dxy = {{0,-1},{0,1},{1,0},{-1,0}};
    auto getid = [&](int i, int j, int tp) {
        return tp*m*n+i*m+j;
    };    
    auto check = [&](int i, int j) {
        if(i < 0 || i >= n || j < 0 || j >= m) return false;
        if(s[i][j] == '1') return false;
        return true;
    };
    
    int S =2*n*m, T=S+1, cnt = 0;
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < m; j++) {
            if(s[i][j] == '1') continue;
            if(s[i][j] == '2') {
                ++cnt;
                flow.addEdge(S, getid(i,j,0), 1);
                for(auto [dx,dy]: dxy) {
                    if(!check(i+dx,j+dy)) continue;
                    flow.addEdge(getid(i, j, 0), getid(i+dx, j+dy, 1), 1);
                }
            }
            flow.addEdge(getid(i, j, 1), T, 1);
        }
    }
    int ans = flow.maxFlow(S, T);
    if(ans != cnt) cout << "No\n";
    else cout << "Yes\n";
}
