void solve() {
	int n, d;
    cin >> n >> d;
    vector<int> a(n);
    cin >> a;
    int mx = *max_element(a.begin(), a.end());
    SegmentTree<Info> seg(mx + 1);
    vector<int> dp(n);
    int ans = 0;
    for(int i = 0; i < n; i++) {
    	int val = seg.rangeQuery(max(0, a[i] - d), min(mx, a[i] + d)+1).mx;
    	dp[i] = val + 1;
    	seg.modify(a[i], {dp[i]});
    	ans = max(ans, dp[i]);
    }
    cout << ans << "\n";
}
