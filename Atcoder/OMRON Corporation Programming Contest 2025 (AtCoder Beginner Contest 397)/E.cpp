void solve() {
  int n, k;
  cin >> n >> k;
  vector G(n * k + 1,vector<int>());
  for(int i = 1; i <= n * k - 1; i++) {
  	int u, v;
  	cin >> u >> v;
  	G[u].push_back(v);
  	G[v].push_back(u);
  }
  if(k == 1) {
  	cout << "Yes\n";
  	return ;
  }
  int ans = 0;
  vector<int> f(n * k + 1);
  int bad = 0;
  function<void(int,int)> dfs = [&](int u, int par) {
  	vector<int> tmp;
  	for(auto v: G[u]) {
  		if(v == par) continue;
  		dfs(v, u);
  		if(f[v] == 0) continue;
  		tmp.push_back(f[v]);
  	}
  	if(tmp.size() >= 3) {
  		bad = 1;
  	}else if(tmp.size() == 2) {
  		if(tmp[0] + tmp[1] + 1 != k) bad = 1;
  		else f[u] = 0;
  	}else if(tmp.size() == 1) {
  		if(tmp[0] + 1 == k) f[u] = 0;
  		else if(tmp[0] + 1 > k) bad = 1;
  		else f[u] = tmp[0] + 1;
  	}else {
  		f[u] = 1;
  	}
  };
  dfs(1, 0);
  cout << (bad ? "No\n":"Yes\n");
}
