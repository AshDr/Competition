LL qpow(LL base, LL x, LL mod) {
    LL res = 1;
    base %= mod;
    while(x) {
       if(x & 1) res = (res * base) % mod;
       base = (base * base) % mod;
       x >>= 1;
    } 
    return res;
}
LL exgcd(LL a,LL b,LL &x,LL &y) {
    if(b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    LL d = exgcd(b,a % b,y,x);
    y -= a / b * x;
    return d;
}// (get inv) gcd(a,p) = 1 

const int N = 1e6 + 10;
const int M = 1e5 + 10;
const int INF = 2147483647;
const LL MOD = 1e9 + 7;
int TT = 1;
LL n,m;
int cnt[N];
void solve() {
	cin >> n >> m;
    //(1+n-1)*(n-1)/2	
    for(int i = 1;i <= m; i++) {
        int u,v;
        cin >> u >> v;
        if(m!=n*(n-1)/2&&m>=(n-1)*(n-2)/2) cnt[u]++,cnt[v]++;
    }
    if(n == 1) {
        cout << 0 << "\n";
    }
    else if(m == n*(n-1)/2) cout << 0 << "\n";//完全图
    else if(m < (n-1)*(n-2)/2) cout << -2 << "\n";//不存在Kn-1
    else {
        LL num = n*(n-1)/2-m;
        int f = 0;
        for(int i = 1; i <= n; i++) if(num == n-1-cnt[i]) f = 1;
        if(f) cout << -1 << "\n";//补图为菊花图
        else cout << -2 << "\n";//否则就是补图存在K3和K2的情况
        for(int i = 1; i <= n; i++) cnt[i] = 0;
    }
}
