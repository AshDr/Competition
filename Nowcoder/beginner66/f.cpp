bool check(int idx) {
    priority_queue<int> heap;
    for(int i=1;i<=n;i++){
        if(i!=idx) heap.push(a[i].first);        
    }
    int final=-1;
    while(!heap.empty()){
        int x=heap.top();heap.pop();
        if(!heap.empty()){
            int y=heap.top();heap.pop();
            heap.push((x+y)/2);
        }else{
            final=x;
            break;
        }
    }
    return final<=a[idx].first;
}
