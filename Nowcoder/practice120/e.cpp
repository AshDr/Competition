int TT = 1;
struct node {
	int childs[2];
	ll sum;
}trie[N];
int tot;
void insert(string s, int num) {
	int curpos = 0;
	for(int i = 0; i < sz(s); i++) {
		int x = s[i] - '0';
		if(!trie[curpos].childs[x]) trie[curpos].childs[x] = ++tot;
		curpos = trie[curpos].childs[x];
	}
	trie[curpos].sum += num;
}
ll ans = 0;
void dfs(int u) {
	vector<ll> vec;
	for(int i = 0; i < 2; i++) {
		int v = trie[u].childs[i]; 
		if(v) {
			dfs(v);
			trie[u].sum += trie[v].sum; 
			vec.push_back(trie[v].sum);
		}
	}
	if(trie[u].childs[0] && trie[u].childs[1]) {
		sort(all(vec));
		ans += vec[0];
	}
}
void solve() {
    int n, m;
    cin >> n >> m;
    ans = 0;tot = 0;
    for(int i = 0; i < n; i++) {
    	string s;int c;
    	cin >> s >> c;
    	insert(s, c);
    }
    dfs(0);
    cout << ans << "\n";
    for(int i = 0; i <= tot; i++) {
    	trie[i].childs[0] = trie[i].childs[1] = trie[i].sum = 0;
    }
}
