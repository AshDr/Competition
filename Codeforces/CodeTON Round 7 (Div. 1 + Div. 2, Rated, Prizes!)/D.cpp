int TT = 1;
int a[N];
void solve() {
    int n, m;
    cin >> n >> m;
    set<int> ones;
    ll sum = 0;
    for(int i = 1; i <= n; i++) {
    	cin >> a[i];
    	if(a[i] == 1) ones.insert(i);
    	sum += a[i];
    }
    for(int i = 1; i <= m; i++) {
    	int op,pos,val;
    	cin >> op;
    	if(op == 1) {
    		cin >> val;
    		if(ones.empty()) {
    			cout << (val % 2 == 0 ? "YES\n":"NO\n");
    		}else {

    			int cnt = min(*ones.begin() - 1, (n - *ones.rbegin()));
    			int t = sum - cnt * 2;
    			if(val <= t) cout << "YES\n";
    			else {
    				if(val % 2 == t % 2 && (val - t) / 2 <= cnt) {
    					cout << "YES\n";
    				}else {
    					cout << "NO\n";
    				}
    			}

    			
    		}
    	}else {
    		cin >> pos >> val;
    		if(a[pos] == 1) ones.erase(pos);
    		if(val == 1) ones.insert(pos);
    		sum += val - a[pos];
    		a[pos] = val;
    	}
    }

}
