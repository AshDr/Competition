struct Time {
  int hour, minute;
  bool operator < (const Time& rhs) {
    if(hour == rhs.hour) {
      return minute < rhs.minute;
    }else return hour < rhs.hour;
  }
  bool operator == (const Time& rhs) {
    return hour == rhs.hour && minute == rhs.minute;
  }
  Time operator + (const int min) {
    Time z;
    z.hour = hour;z.minute = minute;
    z.minute += min;
    z.hour += z.minute / 60;
    z.minute %= 60;
    return z;
  }
};
